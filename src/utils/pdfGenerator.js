// Generate Bulk PAYE Report
export const generateBulkPayeReport = (employees, totals, companyInfo = {}) => {
  const doc = new jsPDF();
  
  // Prepare company info for header
  const headerInfo = {
    name: companyInfo.company_name || 'Not specified',
    tin: companyInfo.company_tin || null,
    month: companyInfo.payroll_month || null,
    year: companyInfo.payroll_year || new Date().getFullYear().toString(),
    tax_authority: companyInfo.tax_authority || null,
    employer_contact: companyInfo.employer_contact || null,
    employer_address: companyInfo.employer_address || null
  };
  
  let yPos = addTaxpayerHeader(doc, 'Bulk PAYE Tax Calculation Report', headerInfo);
  
  // Summary Section
  doc.setFontSize(14);
  doc.setFont('helvetica', 'bold');
  doc.text('Payroll Summary', 20, yPos);
  yPos += 10;
  
  const summaryData = [
    ['Company Name', companyInfo.company_name || 'Not specified'],
    ['Company TIN', companyInfo.company_tin || 'Not specified'],
    ['Tax Authority', companyInfo.tax_authority || 'Not specified'],
    ['Payroll Period', companyInfo.payroll_month && companyInfo.payroll_year 
      ? `${companyInfo.payroll_month} ${companyInfo.payroll_year}` 
      : 'Not specified'],
    ['Total Employees Processed', totals.employeeCount.toString()],
    ['Total Monthly Gross Pay', formatCurrency(totals.totalGross)],
    ['Total Monthly Deductions', formatCurrency(totals.totalDeductions || 0)],
    ['Total Monthly Tax (PAYE)', formatCurrency(totals.totalTax)],
    ['Total Monthly Net Pay', formatCurrency(totals.totalNet)],
    ['Total Annual Tax', formatCurrency(totals.totalTax * 12)]
  ];
  
  autoTable(doc, {
    head: [['Summary Item', 'Amount/Count']],
    body: summaryData,
    startY: yPos,
    theme: 'grid',
    headStyles: { fillColor: [34, 197, 94], textColor: [255, 255, 255] },
    margin: { left: 20, right: 20 }
  });
  
  yPos = doc.lastAutoTable.finalY + 20;
  
  // Employee Details Section with expanded columns
  doc.setFontSize(14);
  doc.setFont('helvetica', 'bold');
  doc.text('Employee Tax Details', 20, yPos);
  yPos += 10;
  
  const calculatedEmployees = employees.filter(emp => emp.calculated && emp.result);
  
  // Expanded employee data with deductions breakdown
  const employeeData = calculatedEmployees.map((emp, index) => [
    (index + 1).toString(),
    emp.name,
    emp.tin || 'N/A',
    formatCurrency(emp.result.monthly_gross_income),
    formatCurrency(emp.result.total_reliefs || 0),
    formatCurrency(emp.result.monthly_tax),
    formatCurrency(emp.result.monthly_net_income)
  ]);
  
  autoTable(doc, {
    head: [['#', 'Employee Name', 'TIN', 'Monthly Gross', 'Total Deductions', 'Monthly Tax', 'Monthly Net']],
    body: employeeData,
    startY: yPos,
    theme: 'grid',
    headStyles: { fillColor: [0, 0, 0], textColor: [255, 255, 255] },
    margin: { left: 20, right: 20 },
    styles: { fontSize: 8 }
  });
  
  // Add individual employee deduction breakdown on new page if more than summary is needed
  if (calculatedEmployees.length > 0 && calculatedEmployees.length <= 10) {
    doc.addPage();
    yPos = 20;
    
    doc.setFontSize(14);
    doc.setFont('helvetica', 'bold');
    doc.text('Individual Deduction Breakdown', 20, yPos);
    yPos += 15;
    
    calculatedEmployees.forEach((emp, index) => {
      if (yPos > 250) {
        doc.addPage();
        yPos = 20;
      }
      
      doc.setFontSize(11);
      doc.setFont('helvetica', 'bold');
      doc.text(`${index + 1}. ${emp.name} (TIN: ${emp.tin || 'N/A'})`, 20, yPos);
      yPos += 8;
      
      const empDeductionData = [
        ['Gross Income', formatCurrency(emp.result.monthly_gross_income)],
        ['Pension (8%)', formatCurrency(emp.pension_contribution || emp.result.monthly_gross_income * 0.08)],
        ['NHF (2.5%)', formatCurrency(emp.nhf_contribution || emp.result.monthly_gross_income * 0.025)],
        ['Life Insurance', formatCurrency(emp.life_insurance_premium || 0)],
        ['Health Insurance', formatCurrency(emp.health_insurance_premium || 0)],
        ['NHIS', formatCurrency(emp.nhis_contribution || 0)],
        ['Rent Relief', formatCurrency(emp.annual_rent ? emp.annual_rent / 12 : 0)],
        ['Mortgage Interest', formatCurrency(emp.mortgage_interest || 0)],
        ['Total Deductions', formatCurrency(emp.result.total_reliefs || 0)],
        ['Taxable Income', formatCurrency(emp.result.taxable_income || 0)],
        ['PAYE Tax', formatCurrency(emp.result.monthly_tax)],
        ['Net Income', formatCurrency(emp.result.monthly_net_income)]
      ];
      
      autoTable(doc, {
        body: empDeductionData,
        startY: yPos,
        theme: 'striped',
        styles: { fontSize: 8 },
        columnStyles: {
          0: { cellWidth: 50, fontStyle: 'bold' },
          1: { cellWidth: 40, halign: 'right' }
        },
        margin: { left: 25, right: 100 }
      });
      
      yPos = doc.lastAutoTable.finalY + 15;
    });
  }
  
  addFiquantFooter(doc);
  
  // Generate filename with company name if available
  const companyNameSlug = companyInfo.company_name 
    ? companyInfo.company_name.replace(/[^a-zA-Z0-9]/g, '_').substring(0, 30) + '_'
    : '';
  const periodSlug = companyInfo.payroll_month && companyInfo.payroll_year 
    ? `${companyInfo.payroll_month}_${companyInfo.payroll_year}_`
    : '';
  
  // Save the PDF
  doc.save(`Bulk_PAYE_Report_${companyNameSlug}${periodSlug}${new Date().toISOString().split('T')[0]}.pdf`);
};

