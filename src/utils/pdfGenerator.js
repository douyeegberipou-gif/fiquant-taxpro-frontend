import { jsPDF } from 'jspdf';
import autoTable from 'jspdf-autotable';

// Utility function to format currency for PDF (with proper encoding)
const formatCurrency = (amount) => {
  // Handle null, undefined, and NaN values
  if (amount === null || amount === undefined || isNaN(amount) || amount === '') {
    return 'N 0.00';
  }
  
  // Convert to number and handle edge cases
  const numAmount = parseFloat(amount);
  if (isNaN(numAmount)) {
    return 'N 0.00';
  }
  
  return `N ${numAmount.toLocaleString('en-NG', { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
};

// Utility function to format percentage
const formatPercentage = (rate) => {
  if (rate === null || rate === undefined || isNaN(rate) || rate === '') {
    return '0.00%';
  }
  
  const numRate = parseFloat(rate);
  if (isNaN(numRate)) {
    return '0.00%';
  }
  
  return `${(numRate * 100).toFixed(2)}%`;
};

// Utility function to format date
const formatDate = () => {
  return new Date().toLocaleDateString('en-NG', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  });
};

// Add taxpayer-focused header
const addTaxpayerHeader = (doc, title, taxpayerInfo) => {
  // Document title
  doc.setFontSize(18);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(0, 0, 0);
  doc.text(title, 20, 20);
  
  // Taxpayer Information Section
  let yPos = 35;
  doc.setFontSize(12);
  doc.setFont('helvetica', 'bold');
  doc.text('Taxpayer Information', 20, yPos);
  yPos += 8;
  
  // Create taxpayer info table
  const taxpayerData = [];
  if (taxpayerInfo.name) {
    taxpayerData.push(['Taxpayer Name:', taxpayerInfo.name]);
  }
  if (taxpayerInfo.tin) {
    taxpayerData.push(['Tax ID (TIN):', taxpayerInfo.tin]);
  }
  if (taxpayerInfo.month) {
    taxpayerData.push(['Period/Month:', taxpayerInfo.month]);
  }
  if (taxpayerInfo.year) {
    taxpayerData.push(['Year of Assessment:', taxpayerInfo.year]);
  }
  if (taxpayerInfo.state) {
    taxpayerData.push(['State of Residence:', taxpayerInfo.state]);
  }
  if (taxpayerInfo.tax_authority) {
    taxpayerData.push(['Tax Authority:', taxpayerInfo.tax_authority]);
  }
  if (taxpayerInfo.employer_contact) {
    taxpayerData.push(['Employer Contact:', taxpayerInfo.employer_contact]);
  }
  if (taxpayerInfo.employer_address) {
    taxpayerData.push(['Employer Address:', taxpayerInfo.employer_address]);
  }
  
  // Add taxpayer info table
  if (taxpayerData.length > 0) {
    autoTable(doc, {
      startY: yPos,
      head: [],
      body: taxpayerData,
      theme: 'plain',
      styles: {
        fontSize: 10,
        cellPadding: 2
      },
      columnStyles: {
        0: { fontStyle: 'bold', cellWidth: 50 },
        1: { cellWidth: 100 }
      },
      margin: { left: 20, right: 20 } // Add proper margins to align with rest of document
    });
    yPos = doc.lastAutoTable.finalY + 10;
  }
  
  // Generated date
  doc.setFontSize(9);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(100, 100, 100);
  doc.text(`Generated on: ${formatDate()}`, 20, yPos);
  
  // Return the Y position where content should start
  return yPos + 15;
};

// Add Fiquant footnote footer
const addFiquantFooter = (doc) => {
  const pageCount = doc.internal.getNumberOfPages();
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    
    // Add separator line
    doc.setDrawColor(200, 200, 200);
    doc.line(20, 275, 190, 275);
    
    // Fiquant branding footnote
    doc.setFontSize(8);
    doc.setTextColor(100, 100, 100);
    doc.setFont('helvetica', 'normal');
    doc.text('Generated by Fiquant TaxPro - Nigerian Tax Calculator 2026', 20, 282);
    doc.text('© 2025 Fiquant Consult. All rights reserved.', 20, 287);
    
    // Page number
    doc.text(`Page ${i} of ${pageCount}`, 170, 287);
  }
};

