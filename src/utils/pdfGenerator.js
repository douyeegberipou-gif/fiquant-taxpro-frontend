// CGT Report Generator
export const generateCgtReport = (cgtInput, cgtResult) => {
  const doc = new jsPDF();
  
  // Prepare taxpayer info for header
  const taxpayerInfo = {
    name: cgtResult.taxpayer_name || 'Not specified',
    year: cgtResult.year || 'Not specified'
  };
  
  let yPos = addTaxpayerHeader(doc, 'Capital Gains Tax Report', taxpayerInfo);
  
  // Asset Transaction Details
  doc.setFontSize(14);
  doc.setFont('helvetica', 'bold');
  doc.text('Asset Transaction Details', 20, yPos);
  yPos += 10;
  
  const isLoss = cgtResult.is_loss || cgtResult.capital_gain < 0;
  const transactionData = [
    ['Disposal Proceeds', formatCurrency(cgtResult.disposal_proceeds || cgtResult.net_proceeds)],
    ['Acquisition Cost', formatCurrency(cgtResult.acquisition_cost)],
    ['Improvement Costs', formatCurrency(cgtResult.improvement_costs || cgtResult.allowable_expenses || 0)],
    ['Selling Expenses', formatCurrency(cgtResult.selling_expenses || 0)],
    ['Total Cost', formatCurrency(cgtResult.total_cost)],
    [isLoss ? 'Capital Loss' : 'Capital Gain', formatCurrency(Math.abs(cgtResult.capital_gain))]
  ];
  
  autoTable(doc, {
    head: [['Transaction Component', 'Amount']],
    body: transactionData,
    startY: yPos,
    theme: 'grid',
    headStyles: { fillColor: [34, 197, 94], textColor: [255, 255, 255] },
    margin: { left: 20, right: 20 }
  });
  
  yPos = doc.lastAutoTable.finalY + 15;
  
  // CGT Calculation
  doc.setFontSize(14);
  doc.setFont('helvetica', 'bold');
  doc.text('CGT Calculation', 20, yPos);
  yPos += 10;
  
  let cgtData = [];
  
  // Helper to clean Naira symbols for PDF compatibility
  const cleanNairaSymbol = (text) => {
    if (!text) return text;
    return text.replace(/₦/g, 'N ').replace(/\u20a6/g, 'N ');
  };
  
  if (cgtResult.is_exempt) {
    cgtData = [
      ['CGT Status', 'EXEMPT'],
      ['Exemption Reason', cleanNairaSymbol(cgtResult.exemption_reason)],
      ['CGT Liability', formatCurrency(0)],
      ['Tax Saved', formatCurrency(cgtResult.tax_saved || 0)]
    ];
  } else if (cgtResult.is_loss || cgtResult.capital_gain < 0) {
    cgtData = [
      ['CGT Status', 'CAPITAL LOSS'],
      ['Loss Amount', formatCurrency(Math.abs(cgtResult.capital_gain || cgtResult.loss_amount || 0))],
      ['CGT Liability', formatCurrency(0)],
      ['Loss Carry Forward', 'Available for future gains']
    ];
  } else {
    const rateDisplay = cgtResult.rate_type === 'flat_30%' ? '30% (Company Rate)' : 
                        `${cgtResult.cgt_rate || 0}% (${cgtResult.rate_type || 'Progressive'})`;
    cgtData = [
      ['Taxable Capital Gain', formatCurrency(cgtResult.capital_gain)],
      ['CGT Rate Applied', rateDisplay],
      ['CGT Liability', formatCurrency(cgtResult.cgt_amount || cgtResult.cgt_liability || 0)]
    ];
  }
  
  autoTable(doc, {
    head: [['CGT Component', 'Amount/Status']],
    body: cgtData,
    startY: yPos,
    theme: 'grid',
    headStyles: { fillColor: [59, 130, 246], textColor: [255, 255, 255] },
    margin: { left: 20, right: 20 }
  });
  
  // Add exemption note if applicable
  if (cgtResult.is_exempt) {
    yPos = doc.lastAutoTable.finalY + 15;
    doc.setFontSize(12);
    doc.setFont('helvetica', 'bold');
    doc.text('Exemption Information', 20, yPos);
    yPos += 8;
    
    doc.setFontSize(10);
    doc.setFont('helvetica', 'normal');
    doc.text('CGT exemption applies when disposal proceeds < N150M AND capital gains <= N10M', 20, yPos);
    yPos += 5;
    doc.text('within any 12-month period (Nigeria Tax Act 2025).', 20, yPos);
  }
  
  // Add tax rate information
  if (!cgtResult.is_exempt && !(cgtResult.is_loss || cgtResult.capital_gain < 0)) {
    yPos = doc.lastAutoTable.finalY + 15;
    doc.setFontSize(12);
    doc.setFont('helvetica', 'bold');
    doc.text('Tax Rate Information (NTA 2025)', 20, yPos);
    yPos += 8;
    
    doc.setFontSize(10);
    doc.setFont('helvetica', 'normal');
    if (cgtResult.taxpayer_type === 'company') {
      doc.text('Companies: 30% CGT rate (aligned with Corporate Income Tax rate)', 20, yPos);
    } else {
      doc.text('Individuals: Progressive rates (0-25%) based on total income level', 20, yPos);
    }
  }
  
  addFiquantFooter(doc);
  
  // Save the PDF
  doc.save(`CGT_Report_${cgtResult.taxpayer_name || 'Taxpayer'}_${cgtResult.year || 'Report'}_${new Date().toISOString().split('T')[0]}.pdf`);
};